package PageObjectModel;

public class Sumof100 {
    public static void main(String[] args) {
        int a[]={10,20,30,40,50,60,70,90,80,100};
        int length=a.length;
        int sum=0;
        for(int i=0;i<=length-1;i++){
            sum=sum+1;

        }
        System.out.println(sum);

    }
}
